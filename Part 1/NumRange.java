import java.util.Scanner;



class NumRange
{

	
	public static void main(String[] args) 
	{
	    
	    
		
		Scanner s = new Scanner(System.in);
		int L = s.nextInt();

		int R = s.nextInt();
		for(int i=L;i<=R;i++)
        
		{
            
			System.out.println(i);
        
		}
        
        
        

		
		s.close();
	
	}

}